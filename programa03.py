# Calculadora python

num1 = int(input ("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

soma = num1 + num2
print(f"A soma de {num1} e {num2} é igual a = {soma}")

subtracao = num1 - num2
print(f"A subtração de {num1} e {num2} é igual a = {subtracao}")

multiplicacao = num1 * num2
print(f"A multiplicação de {num1} e {num2} é igual a = {multiplicacao}")

divisao = num1 / num2
print(f"A divisão de {num1} e {num2} é igual a = {divisao:.2f}")

resto_divisao = num1 % num2
print(f"O resto da divisão {num1} e {num2} é igual a = {resto_divisao}")

potenciacao = num1 ** num2
print(f"A potência de {num1} e {num2} é igual a = {potenciacao:.2f}")

