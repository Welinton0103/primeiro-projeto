while True:
    print (" 1 - somar")
    print(" 2 - subtrair")
    print(" 3 - multiplicar")
    print(" 4 - dividir")
    print(" 5 - sair")

    opcao = int(input("Digite sua opcao: "))

    if opcao == 1:
        print("SOMAR")

    elif opcao == 2:
        print("Subtrair")

    elif opcao == 3:
        print("Multiplicar")

    elif opcao == 4:
        print("Dividir")

    elif opcao == 5:
        print("Saindo...")
        break

    else:
        print("Opcao invalida, tente outra")




